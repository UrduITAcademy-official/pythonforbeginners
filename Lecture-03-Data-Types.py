# Float
a = 2.5
print(a)
print("Type of a:", type(a))

# Complex Number
a = 20j
print(a)
print("Type of a:", type(a))

# String
a = "hello"
print(a)
print("Type of a:", type(a))

# List
a = ['apple', 'banana', 'cherry']
print(a)
print("Type of a:", type(a))

# Tuple
a = (10, 20, 30)
print(a)
print("Type of a:", type(a))

# Boolean
a = True
print(a)
print("Type of a:", type(a))

# Dictionary
a = {'name': 'Ali', 'age': 25}
print(a)
print("Type of a:", type(a))
